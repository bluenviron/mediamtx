    /// <summary>
    /// Annex K quality scaler: maps quality (1..100) to a multiplier
    /// applied to the reference Q-tables. Output is then clamped per
    /// element to <c>[1, 255]</c> for 8-bit DQT.
    /// </summary>
    public static byte[] ScaleQuantTable(ReadOnlySpan<byte> reference, int quality)
    {
        if (quality < 1) quality = 1;
        if (quality > 100) quality = 100;
        int scale = quality < 50 ? 5000 / quality : 200 - quality * 2;

        var output = new byte[64];
        for (int i = 0; i < 64; i++)
        {
            int v = (reference[i] * scale + 50) / 100;
            if (v < 1) v = 1;
            else if (v > 255) v = 255;
            output[i] = (byte)v;
        }
        return output;
    }
