// https://datatracker.ietf.org/doc/html/rfc2631#section-2.1.5
func dhValidatePublicKey(key []byte) error {
	// 1. y >= 2 && y < p
	var y big.Int
	y.SetBytes(key)
	var two big.Int
	two.SetUint64(2)
	r := y.Cmp(&two)
	if r < 0 {
		return fmt.Errorf("key is < 2")
	}
	var p big.Int
	p.SetBytes(p1024)
	r = y.Cmp(&p)
	if r >= 0 {
		return fmt.Errorf("key is >= p")
	}

	// 2. (y^q mod p) == 1
	var q big.Int
	q.SetBytes(q1024)
	var z big.Int
	z.Exp(&y, &q, &p)
	var one big.Int
	one.SetUint64(1)
	r = z.Cmp(&one)
	if r != 0 {
		return fmt.Errorf("y^q mod p is != 1")
	}

	return nil
}

func dhGenerateKeyPair() ([]byte, []byte, error) {
	priv := make([]byte, dhKeyLength)
	_, err := rand.Read(priv)
	if err != nil {
		return nil, nil, err
	}

	// y = g ^ x mod p
	var g big.Int
	g.SetUint64(2)
	var x big.Int
	x.SetBytes(priv)
	var p big.Int
	p.SetBytes(p1024)
	var y big.Int
	y.Exp(&g, &x, &p)
	pub := y.Bytes()

	return priv, pub, nil
}

// https://datatracker.ietf.org/doc/html/rfc2631#section-2.1.1
func dhComputeSharedSecret(priv []byte, pub []byte) []byte {
	// ZZ = (ya ^ xb)  mod p
	var y big.Int
	y.SetBytes(pub)
	var x big.Int
	x.SetBytes(priv)
	var p big.Int
	p.SetBytes(p1024)
	var z big.Int
	z.Exp(&y, &x, &p)
	return z.Bytes()
}
